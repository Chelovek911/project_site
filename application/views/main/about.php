                        <h4><strong>ПОДРОБНЕЕ О ТМС «LESOL»</strong></h4>
                        <p>«Lesol» - это линейка технических моющих средств отечественного производства. Их состав был разработан, создан и испытан с применением новейших технологий и полностью отвечает всем требованиям нормативно-технической документации.</p>
                        <p>Профессиональные ТМС «Lesol» применяются как для удаления сильных загрязнений и труднорастворимых отложений различного происхождения, так и для проведения плановых дезинфекционных мероприятий в соответствии с санитарными нормами на различных предприятиях легкой и тяжелой отраслей промышленности, в транспортных и коммунальных хозяйствах, а также в сфере обслуживания (гостиницы, рестораны, клининг).</p>
