                            <li><span style="background-color: transparent;">защиты кожи от вредных воздействий агрессивных сред и перепадов температур;</span></li>
                            <li><span style="background-color: transparent;">ухода за кожей, постоянно подвергающейся агрессивным температурным и химическим воздействиям;</span></li>
                            <li><span style="background-color: transparent;">удаления технологических производственных загрязнений.</span></li>
