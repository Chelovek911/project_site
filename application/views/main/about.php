                            <li>соответствие высоким экологическим стандартам: компоненты моющих средств «Lesol» биоразлагаемы, не образуют в сточных водах вредных химических соединений, пожаро- и взрывобезопасны;</li>
                            <li>соответствие стандартам качества: все сырье, используемое в производстве, соответствует требованиям ВОЗ (Всемирная Организация Здоровья) Европейского сообщества, имеет сертификаты качества, паспорта безопасности и заключения государственной санитарно-гигиенической экспертизы РФ.</li>
                            <li>контроль качества: наши технологи-аналитики обеспечивают контроль качества на всех стадиях производства продукции (сырье – технологический процесс – готовая продукция);</li>
                            <li>самосовершенствование: регулярное обновление оборудования, повышение эффективности научно-исследовательских работ, разработка и внедрение новых технологических решений;</li>
