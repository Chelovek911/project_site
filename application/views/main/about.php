                        <h4><strong>ПОДРОБНЕЕ О ДСИЗ «GEСO»</strong></h4>
                        <p>Название «GECO» было дано нашему продукту отнюдь не случайно. Геккон — небольшая, но удивительная рептилия, кожа которой обладает особенным покрытием, наделяющим ее гидрофобными свойствами. Она быстро регенерирует и отталкивает бактерии, а также самоочищается. Уникальность геккона в том, что он — единственный представитель позвоночных, обладающий гидрофобной кожей. Результат упорного труда наших ученых, вдохновленных самой природой — это уникальность дерматологических средств индивидуальной защиты «GECO», которые мы с гордостью и удовольствием представляем вам сегодня. Наши средства удобны в применении, не вызывают дискомфорта при использовании и легко удаляются. Защитные, очищающие и восстанавливающие средства серии органично сочетают в себе активные природные компоненты и новейшие научные разработки в соответствующей области, отвечая всем требованиям действующей нормативно­технической документации. В состав всех препаратов, представленных в линейке ДСИЗ «GEСO», входит инновационный комплекс «GECO­-CARE». Его основные компоненты, d­пантенол и α­токоферол ацетат, усиливают действие друг друга. Они подобраны в специальном соотношении, что приводит к уникальному синергетическому эффекту, который обуславливает высокую эффективность средств и обеспечивает стимуляцию быстрой эпитализации и заживления повреждённых тканей. Помимо ранозаживляющего, данный комплекс оказывает противовоспалительное, успокаивающее и длительное увлажняющее воздействие; снижает раздражающий и сенсибилизирующий эффект, вызванный техногенными и экологическими факторами, а также влиянием солнечного излучения. Подходит для людей с чувствительной кожей. Дерматологические средства индивидуальной защиты «GEСO» применяются на различных промышленных предприятиях и производствах для:</p>
